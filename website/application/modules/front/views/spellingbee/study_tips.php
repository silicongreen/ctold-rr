<h2 class="f2" style="color:#FFCA0A;">About Spelling Bee</h2>

<div class="col-lg-12">
    
    <ul>
        <li>
            <p>
                Spellato this year in launched in an App format also can be found in <a href="#">PDF</a> format on Champs21.com website. You can download this the app on your Android device from <a href="#">Google Play Store</a>.
            </p>
        </li>
        <li>
            <p>
                Go through the new Spellato thoroughly, particularly the <strong><i>‘Word Watch’</i></strong> section of Spellato 2015. In this section you will find the <strong><i>suggestion words</i></strong> which are bound to show up in the Spelling Bee 2015 competition. The Spellato also contains all the words used in the previous Spelling Bee Seasons, which will give you a solid idea about the competition.
            </p>
        </li>
        <li>
            <p>
                Always remember “practice makes perfect”; hence the more you practice, the more you’ll learn and be more confident eventually. <strong><i>Increase your English reading habits;</i></strong> this could be from English newspapers (www.thedailystar.net), novels, or elsewhere. Try understanding the meaning of each word and their application to sentences.
            </p>
        </li>
        <li>
            <p>
                At the same time, <strong><i>set up a target of learning twenty new words every day</i></strong>. Get yourself the new Spellato or just a dictionary and maintain a pocket journal for learning words. Keep a specific time every day for testing your own spelling skills.
            </p>
        </li>
        <li>
            <p>
                Make <strong><i>a list of your commonly misspelled words</i></strong>. When you catch yourself spelling the same word wrong over and over, write it down somewhere. When you get a chance, look it up and put the correct spelling next to it. (Make sure you mark which is correct!)
            </p>
        </li>
        <li>
            <p>
                The only way to really learn a word is to use it. When you look up how to spell a word, <strong><i>write it down several times in a row</i></strong>, and do it again a day or two later – you’re trying to build up the motor memory of writing it correctly spelled. Write a blog, a journal, an email, or anything that will keep you using words – and pay special attention as you write to the words that come up wrong using your spell checker.
            </p>
        </li>
        <li>
            <p>
                Bear in mind that all the spellings used in Spelling Bee are in <strong><i>British English</i></strong>.
            </p>
        </li>
    </ul>
    
</div>